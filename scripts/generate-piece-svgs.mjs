// Regenerate src/utils/pieceSvgs.generated.ts from react-chessboard's pieces.
// Run after react-chessboard upgrades: node scripts/generate-piece-svgs.mjs
import { writeFileSync } from 'node:fs'
import { renderToStaticMarkup } from 'react-dom/server'
import { defaultPieces } from 'react-chessboard'

const CODES = ['wP','wN','wB','wR','wQ','wK','bP','bN','bB','bR','bQ','bK']
const entries = CODES.map(
  (code) => `  ${code}: ${JSON.stringify(renderToStaticMarkup(defaultPieces[code]({})))},`,
).join('\n')
const out = `// AUTO-GENERATED by scripts/generate-piece-svgs.mjs — do not edit by hand.
// Piece artwork from react-chessboard's defaultPieces (45x45 viewBox each).
export const PIECE_MARKUP: Record<string, string> = {
${entries}
}
`
writeFileSync(new URL('../src/utils/pieceSvgs.generated.ts', import.meta.url), out)
console.log('wrote src/utils/pieceSvgs.generated.ts')
